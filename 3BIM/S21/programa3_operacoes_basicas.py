import numpy as np

array1 = np.array([1, 2])
array2 = np.ones(2, dtype=int)

# subtraindo o array1 do array2
subtracao = array1 - array2
print('Subtração: ', subtracao)

# Multiplicando o array1 por ele mesmo
multiplicacao = array1 * array1
print('Multiplicação: ', multiplicacao)

# Dividindo o array por ele mesmo
divisao = array1 / array1
print('Divisão: ', divisao)
